#include <stdio.h>

/**
 * main - print the size of various sizes on computer it's compiled on
 * Return: 0 as success value exit status
 */
int main(void)
{
	printf("Size of a char: %c byte(s)", sizeof(char));
	printf("\nSize of an int: %d byte(s)", sizeof(char));
	printf("\nSize of a long: %ld byte(s)", sizeof(char));
	printf("\nSize of a long long int: %d byte(s)", sizeof(char));
	printf("\nSize of a float: %f byte(s)", sizeof(char));
	return (0);
}
