#include "3-calc.h"

/**
 * op_add - add 2 number
 * @a: 1st num
 * @b: 2nd num
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract 2 number
 * @a: 1st num
 * @b: 2nd num
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply 2 number
 * @a: 1st num
 * @b: 2nd num
 *
 * Return: the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - multiply 2 number
 * @a: 1st num
 * @b: 2nd num
 *
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the remainder of division
 * @a: 1st num
 * @b: 2nd num
 *
 * Return: the modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
