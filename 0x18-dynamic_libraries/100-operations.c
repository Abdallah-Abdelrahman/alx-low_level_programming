/**
 * add - add 2 numbers
 * @a: 1st num
 * @b: 2nd num
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract 2 numbers
 * @a: 1st num
 * @b: 2nd num
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - get the remainder of division
 * @a: 1st num
 * @b: 2nd num
 * Return: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
/**
 * div - divided @a by @b
 * @a: 1st num
 * @b: 2nd num
 * Return: quotient
 */
int div(int a, int b)
{
	return (a / b);
}
